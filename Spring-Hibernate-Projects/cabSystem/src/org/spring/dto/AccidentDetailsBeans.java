package org.spring.dto;

public class AccidentDetailsBeans {

	private int accidentId;
	private int driverId;
	private String vehicleNo;
	private int insuranceDId;
	private int vehicleInsuranceId;
	private String insuredDriver;
	private String insuredVehicle;
	private String accidentDate;
	private String place;
	private String price;
	private String reason;
	private String query;
	
	

	public String getQuery() {
		return query;
	}

	public void setQuery(String query) {
		this.query = query;
	}

	public int getAccidentId() {
		return accidentId;
	}

	public void setAccidentId(int accidentId) {
		this.accidentId = accidentId;
	}

	public int getDriverId() {
		return driverId;
	}

	public void setDriverId(int driverId) {
		this.driverId = driverId;
	}

	public String getVehicleNo() {
		return vehicleNo;
	}

	public void setVehicleNo(String vehicleNo) {
		this.vehicleNo = vehicleNo;
	}

	public int getInsuranceDId() {
		return insuranceDId;
	}

	public void setInsuranceDId(int insuranceDId) {
		this.insuranceDId = insuranceDId;
	}

	public int getVehicleInsuranceId() {
		return vehicleInsuranceId;
	}

	public void setVehicleInsuranceId(int vehicleInsuranceId) {
		this.vehicleInsuranceId = vehicleInsuranceId;
	}

	public String getInsuredDriver() {
		return insuredDriver;
	}

	public void setInsuredDriver(String insuredDriver) {
		this.insuredDriver = insuredDriver;
	}

	public String getInsuredVehicle() {
		return insuredVehicle;
	}

	public void setInsuredVehicle(String insuredVehicle) {
		this.insuredVehicle = insuredVehicle;
	}

	public String getAccidentDate() {
		return accidentDate;
	}

	public void setAccidentDate(String accidentDate) {
		this.accidentDate = accidentDate;
	}

	public String getPlace() {
		return place;
	}

	public void setPlace(String place) {
		this.place = place;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

}
