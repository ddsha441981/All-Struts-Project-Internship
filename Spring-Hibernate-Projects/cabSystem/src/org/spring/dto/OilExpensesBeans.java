package org.spring.dto;

public class OilExpensesBeans {

	private int oilexId;
	private int driverId;
	private String vehicleNo;
	private String totalKM;
	private String oilLtr;
	private String price;
	private String totalPrice;
	private String oilFillDate;

	public int getOilexId() {
		return oilexId;
	}

	public void setOilexId(int oilexId) {
		this.oilexId = oilexId;
	}

	public int getDriverId() {
		return driverId;
	}

	public void setDriverId(int driverId) {
		this.driverId = driverId;
	}

	public String getVehicleNo() {
		return vehicleNo;
	}

	public void setVehicleNo(String vehicleNo) {
		this.vehicleNo = vehicleNo;
	}

	public String getTotalKM() {
		return totalKM;
	}

	public void setTotalKM(String totalKM) {
		this.totalKM = totalKM;
	}

	public String getOilLtr() {
		return oilLtr;
	}

	public void setOilLtr(String oilLtr) {
		this.oilLtr = oilLtr;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getTotalPrice() {
		return totalPrice;
	}

	public void setTotalPrice(String totalPrice) {
		this.totalPrice = totalPrice;
	}

	public String getOilFillDate() {
		return oilFillDate;
	}

	public void setOilFillDate(String oilFillDate) {
		this.oilFillDate = oilFillDate;
	}

}
