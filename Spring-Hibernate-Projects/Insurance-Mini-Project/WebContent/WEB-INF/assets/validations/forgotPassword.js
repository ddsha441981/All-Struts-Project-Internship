function changePassword()
{
	if(emailValidations() & newPassword() & confPassword())
	{
	alert("First Method Call");
		return true;
	}
	else
	{
		return false;
	} 
	
	/**
	Email Validations of Change Password Validate
	*/
		function emailValidations()
		{
			var x = document.forms["myForm"]["email"].value;
	    var atpos = x.indexOf("@");
	    var dotpos = x.lastIndexOf(".");
	    if(x==""){
	    	document.getElementById("emailError").innerHTML="Email must not be empty";
	    	
	    	return false;
	    }
	    
	    if (atpos<1 || dotpos<atpos+2 || dotpos+2>=x.length) 
	    {
	    	document.getElementById("email").focus();
	        document.getElementById("emailError").innerHTML="Enter a email followed by @";
	        
	        return false;
	    }
	    
	    else
	    {
	    	document.getElementById("emailError").innerHTML="<span class='glyphicon glyphicon-ok'></span>";
	    	
	    	return true;
	    }	
	}
		
	
	/**
	Password Validate
	*/
		function newPassword()
		{
		alert("Main Function call");
			var newChange = document.getElementById("autogeneratedPwd").value;
			if(newChange=="")
			{
				document.getElementById("newChangePasswordError").innerHTML="Password must Not be Empty";
				return false;
			}
			else
			{
				document.getElementById("newChangePasswordError").innerHTML="<span class='glyphicon glyphicon-ok'></span>";
				return true;
			}
		} 
		
		function confPassword()
		{
		alert("Confirm Password Call");
			var autoGen = document.getElementById("autogeneratedPwd").value;
			var confPwd = document.getElementById("confirmPwd").value;
			if(confPwd=="")
			{
			
				document.getElementById("confirmPwdError").innerHTML="Confirm Password Must Not Be Empty!!!";	
				return false;	
			}
			
			else if(autoGen!=confPwd)
			{
				alert("Not Matching");
				document.getElementById("confirmPwd").focus;
				document.getElementById("confirmPwdError").innerHTML="Password Doesn't Match";
				return false;
			}
			
			else
			{
				document.getElementById("confirmPwdError").innerHTML="<span class='glyphicon glyphicon-ok'></span>";
				return true;
			}
			
		}
}