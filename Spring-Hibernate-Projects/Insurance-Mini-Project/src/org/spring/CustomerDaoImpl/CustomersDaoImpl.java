package org.spring.CustomerDaoImpl;

import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;

import org.spring.CustomerBeans.ChangePwdBeans;
import org.spring.CustomerInterface.CustomerDao;
import org.spring.UtilityClass.QueryOfDatabase;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.dao.DataAccessException;
import org.springframework.jdbc.core.JdbcTemplate;
import org.springframework.jdbc.core.PreparedStatementCallback;
import org.springframework.jdbc.core.ResultSetExtractor;
import org.springframework.stereotype.Repository;

@Repository
public class CustomersDaoImpl implements CustomerDao {

	@Autowired(required = true)
	
	JdbcTemplate jdbcTemplate;
	public void setJdbcTemplate(JdbcTemplate jdbcTemplate) {
		this.jdbcTemplate = jdbcTemplate;
	}
	
	@Override
	public Boolean changeCustomersPassword(ChangePwdBeans pwdBeans) {
		
		
		System.out.println("In change Password Dao class"+pwdBeans.getCustId());
		return jdbcTemplate.execute(QueryOfDatabase.updatePass,new PreparedStatementCallback<Boolean>() {

			@Override
			public Boolean doInPreparedStatement(PreparedStatement pstmt) throws SQLException, DataAccessException {
				
				pstmt.setInt(2, pwdBeans.getCustId());
				pstmt.setString(1, pwdBeans.getAutogeneratedPwd());
				System.out.println("Password change Successfully/////////////////////////////");
				
				return pstmt.execute();
			}
		});
	}
	
	
}
