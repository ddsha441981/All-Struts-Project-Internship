package org.spring.LoginBeans;

public class LoginsBeans {

	private int userId;
	private int empId;
	private int customId;
	private String email;
	private String autogeneratedPwd;
	private String userRole;

	

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public int getEmpId() {
		return empId;
	}

	public void setEmpId(int empId) {
		this.empId = empId;
	}

	public int getCustomId() {
		return customId;
	}

	public void setCustomId(int customId) {
		this.customId = customId;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	
	public String getAutogeneratedPwd() {
		return autogeneratedPwd;
	}

	public void setAutogeneratedPwd(String autogeneratedPwd) {
		this.autogeneratedPwd = autogeneratedPwd;
	}

	public String getUserRole() {
		return userRole;
	}

	public void setUserRole(String userRole) {
		this.userRole = userRole;
	}

	

	
}
