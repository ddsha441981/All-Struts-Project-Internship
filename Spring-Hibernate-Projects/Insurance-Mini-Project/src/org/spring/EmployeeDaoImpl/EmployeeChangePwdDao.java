package org.spring.EmployeeDaoImpl;

import java.sql.PreparedStatement;
import java.sql.SQLException;

import org.spring.EmployeeBeans.EmployeeChangePwdBeans;
import org.spring.EmployeeInterface.ChangePasswordOfEmployee;
import org.spring.UtilityClass.QueryOfDatabase;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.dao.DataAccessException;
import org.springframework.jdbc.core.JdbcTemplate;
import org.springframework.jdbc.core.PreparedStatementCallback;
import org.springframework.stereotype.Repository;

@Repository
public class EmployeeChangePwdDao implements ChangePasswordOfEmployee {

	@Autowired(required = true)
	JdbcTemplate jdbcTemplate;
	
	public JdbcTemplate getJdbcTemplate() {
		return jdbcTemplate;
	}

	@Override
	public Boolean changePasswordEmployee(EmployeeChangePwdBeans employeeChangePwdBeans) {
	
		System.out.println("In Dao Class");
		System.out.println("In change Password Dao class"+employeeChangePwdBeans.getEmpId());
		
		return jdbcTemplate.execute(QueryOfDatabase.employeePasswordChnaged,new PreparedStatementCallback<Boolean>() {

			@Override
			public Boolean doInPreparedStatement(PreparedStatement pstmt) throws SQLException, DataAccessException {
				pstmt.setInt(2, employeeChangePwdBeans.getEmpId());
				pstmt.setString(1, employeeChangePwdBeans.getAutogeneratedPwd());
				return pstmt.execute();
			}
		});
	}
	
	
}
