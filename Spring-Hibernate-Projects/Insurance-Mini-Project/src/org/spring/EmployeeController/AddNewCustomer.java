import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;

@Controller
public class AddNewCustomer {
	
	public static Logger  logger = Logger.getLogger(AddNewCustomer.class);
	
	@Autowired(required = true)
	CustomerRegister custmdao;
	
	@RequestMapping(value ="/AddNewCustomers")
	public String newCustomers(){
		
		logger.info("Add New Customers Method SuccessFully Executed");
		return "addnewCustomer";
		
	}
	
	@RequestMapping(value ="/addNewCustomersByEmployees",method = RequestMethod.GET)
	public String addnewCustomers(CustomersBeans customerbens, Model model){
		
		logger.info("Add New Customers Method SuccessFully Executed and Random Password Generated Automatically is Processing");
		
	String autogeneratedPwd = 	RandomPassword.autoGeneratedPassword();
	logger.info("Random Password  Automatically Generated successfully");	
	
	customerbens.setAutogeneratedPwd(autogeneratedPwd);
		
		custmdao.addCustomer(customerbens);
		
	logger.info("Customers Registeration successFully Added");
		
		//MySender.sendMail("kkumawat1111@gmail.com", "user221user@gmail.com", "Good Morning", "Deendayal");
		
		//String emailValues = EmailMail.sendMail(customerbens.getFrom(), customerbens.getCustEmailId(), "Good Morning", "Your UserName is : " +customerbens.getCustName()+"\n Your Password is : "+autogeneratedPwd );
		
	
		
		model.addAttribute("MyPwd", autogeneratedPwd);
		model.addAttribute("msg", "Customers Registeration successFully");
		
		return "employeeProfile";
	}
}
