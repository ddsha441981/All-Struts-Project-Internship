package org.spring.dto;

public class SelectBeans {

	private String registerSelect;
	private String forgotPasswordSelect;

	public String getForgotPasswordSelect() {
		return forgotPasswordSelect;
	}

	public void setForgotPasswordSelect(String forgotPasswordSelect) {
		this.forgotPasswordSelect = forgotPasswordSelect;
	}

	public String getRegisterSelect() {
		return registerSelect;
	}

	public void setRegisterSelect(String registerSelect) {
		this.registerSelect = registerSelect;
	}

}
