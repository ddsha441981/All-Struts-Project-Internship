package org.spring.mvc.beanexample;

import java.util.Date;

public class Policiesdetails {
	private int policyid;
	private String policyname;
	private int policyterm;
	private int policyamount;
	private int policyfaceamount;
	private int policyinterest;
	private String policydate;
	private int bonusperiod;
	private int bonusrate;

	public int getPolicyid() {
		return policyid;
	}

	public void setPolicyid(int policyid) {
		this.policyid = policyid;
	}

	public String getPolicyname() {
		return policyname;
	}

	public void setPolicyname(String policyname) {
		this.policyname = policyname;
	}

	public int getPolicyterm() {
		return policyterm;
	}

	public void setPolicyterm(int policyterm) {
		this.policyterm = policyterm;
	}

	public int getPolicyamount() {
		return policyamount;
	}

	public void setPolicyamount(int policyamount) {
		this.policyamount = policyamount;
	}

	public int getPolicyfaceamount() {
		return policyfaceamount;
	}

	public void setPolicyfaceamount(int policyfaceamount) {
		this.policyfaceamount = policyfaceamount;
	}

	public int getPolicyinterest() {
		return policyinterest;
	}

	public void setPolicyinterest(int policyinterest) {
		this.policyinterest = policyinterest;
	}

	public String getPolicydate() {
		return policydate;
	}

	public void setPolicydate(String policydate) {
		this.policydate = policydate;
	}

	public int getBonusperiod() {
		return bonusperiod;
	}

	public void setBonusperiod(int bonusperiod) {
		this.bonusperiod = bonusperiod;
	}

	public int getBonusrate() {
		return bonusrate;
	}

	public void setBonusrate(int bonusrate) {
		this.bonusrate = bonusrate;
	}

}
