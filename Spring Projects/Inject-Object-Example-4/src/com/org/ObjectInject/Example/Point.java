package com.org.ObjectInject.Example;

/**
 * @author Deendayal Kumawat
 */
public class Point {
	/**
	 * @param create
	 *            instance variable
	 */
	private int x;
	private int y;

	/**
	 * @param Getter
	 *            Setter to get and set value of x
	 */

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	/**
	 * @param Getter
	 *            Setter to get and set value of x
	 */

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

}
