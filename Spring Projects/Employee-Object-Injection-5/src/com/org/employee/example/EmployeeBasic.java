package com.org.employee.example;

/**@author Deendayal Kumawat
 */
public class EmployeeBasic {

	/**@param create instance variable
	 */
	private int empAge;
	private String empAdd;
	private String phone;
	private double empSal;
	private String empEdu;
	private String empExperience;
	
	/**
	 * @param create Getter and Setter to get and set values of EmpAge
	 */
	public int getEmpAge() {
		return empAge;
	}
	public void setEmpAge(int empAge) {
		this.empAge = empAge;
	}
	
	/**
	 * @param create Getter and Setter to get and set values of EmpAddress
	 */ 
	
	public String getEmpAdd() {
		return empAdd;
	}
	public void setEmpAdd(String empAdd) {
		this.empAdd = empAdd;
	}
	
	/**
	 * @param create Getter and Setter to get and set values of Phone
	 */
	
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	
	/**
	 * @param create Getter and Setter to get and set values of EmpSal
	 */
	public double getEmpSal() {
		return empSal;
	}
	public void setEmpSal(double empSal) {
		this.empSal = empSal;
	}
	
	/**
	 * @param create Getter and Setter to get and set values of EmpEducation
	 */
	
	public String getEmpEdu() {
		return empEdu;
	}
	public void setEmpEdu(String empEdu) {
		this.empEdu = empEdu;
	}
	
	/**
	 * @param create Getter and Setter to get and set values of Experience
	 */
	public String getEmpExperience() {
		return empExperience;
	}
	public void setEmpExperience(String empExperience) {
		this.empExperience = empExperience;
	}
	
	
	

}
