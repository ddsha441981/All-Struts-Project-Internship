package org.spring.ExceptionHandling;

public class SpringException extends RuntimeException{
	
	String springExceptionsMsg;

	public String getSpringExceptionsMsg() {
		return springExceptionsMsg;
	}

	public void setSpringExceptionsMsg(String springExceptionsMsg) {
		this.springExceptionsMsg = springExceptionsMsg;
	}

	
	
	

}
