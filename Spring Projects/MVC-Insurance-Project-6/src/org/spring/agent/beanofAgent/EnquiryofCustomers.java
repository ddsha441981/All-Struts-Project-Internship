package org.spring.agent.beanofAgent;

/**
 * @author Deendayal
 * @since 2017
 * @version 1.1.2 This class is EnquiryofCustomers class
 */
public class EnquiryofCustomers {

	private int enquiryId;
	private String customerName;
	private String customaddress;
	private String customMobNo;
	private String enquirydate;
	private int policyId;
	private int agentId;

	public int getEnquiryId() {
		return enquiryId;
	}

	public void setEnquiryId(int enquiryId) {
		this.enquiryId = enquiryId;
	}

	public String getCustomerName() {
		return customerName;
	}

	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}

	public String getCustomaddress() {
		return customaddress;
	}

	public void setCustomaddress(String customaddress) {
		this.customaddress = customaddress;
	}

	public String getCustomMobNo() {
		return customMobNo;
	}

	public void setCustomMobNo(String customMobNo) {
		this.customMobNo = customMobNo;
	}

	public String getEnquirydate() {
		return enquirydate;
	}

	public void setEnquirydate(String enquirydate) {
		this.enquirydate = enquirydate;
	}

	public int getPolicyId() {
		return policyId;
	}

	public void setPolicyId(int policyId) {
		this.policyId = policyId;
	}

	public int getAgentId() {
		return agentId;
	}

	public void setAgentId(int agentId) {
		this.agentId = agentId;
	}

}
